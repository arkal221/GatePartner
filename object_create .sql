

 CREATE TABLE "GATEPARTNER_INBOX" 
   (	"ID" NUMBER(9,0), 
	"SENDER_NAME" VARCHAR2(255 BYTE), 
	"SENDER" VARCHAR2(255 BYTE), 
	"RECEIVER" VARCHAR2(250 BYTE), 
	"SUBJECT" VARCHAR2(255 BYTE),
	"MSG" VARCHAR2(2000 BYTE), 
	"SENTTIME" VARCHAR2(100 BYTE), 
	"RECEIVEDTIME" VARCHAR2(100 BYTE), 
	"OPERATOR" VARCHAR2(120 BYTE), 
	"MSGTYPE" VARCHAR2(160 BYTE), 
	"REFERENCE" VARCHAR2(100 BYTE), 
	"STATUS" VARCHAR2(50 BYTE), 
	"ERRORMSG" VARCHAR2(250 BYTE), 
	"ATTACH" VARCHAR2(250 BYTE),  
	 CONSTRAINT "GATEPARTNER_IN_PK" PRIMARY KEY ("ID")
  );
 

 CREATE TABLE "GATEPARTNER_CONF" 
   (	"CONF_NAME" VARCHAR2(50 BYTE), 
	"CONF_VALUE" VARCHAR2(4000 BYTE), 
	 CONSTRAINT "GATEPARTNER_CF_PK" PRIMARY KEY ("CONF_NAME")
   ); 


 CREATE OR REPLACE TRIGGER "AUTONUM_GATEPARTNER_IN" 
  before INSERT ON GATEPARTNER_INBOX 
  FOR EACH ROW
     WHEN (new.id IS NULL) BEGIN
  SELECT NVL(MAX(id),0) + 1
  INTO :NEW.id
  FROM GATEPARTNER_INBOX;
 END;
 /
 ALTER TRIGGER "AUTONUM_GATEPARTNER_IN" ENABLE;
 

